    private void LoadData(){
        String SQL = "select * from `congto`";
        ResultSet rs = connection.ExcuteQueryGetTable(SQL);
        Object [] obj = new Object[]{"Số CT","Chủ hộ","Số cũ","Số mới","Sô tiêu thụ","Thành tiền"};
        DefaultTableModel tableModel = new DefaultTableModel(obj,0);
        tblCongTo.setModel(tableModel);
        try{
            while(rs.next()){
                Object[] item = new Object[6];
                CongTo hs = new CongTo(rs);
                item[0] = hs.getSoCT();
                item[1] = hs.getChuHo();
                item[2] = hs.getSoCu();
                item[3] = hs.getSoMoi();
                item[4] = hs.getSoDien();
                item[5] = hs.getThanhTien();
                tableModel.addRow(item);
            }
        }
        catch (Exception e) {
            System.out.println(e.toString());
        }
        clearText();
        setWidthColumn();
        disableButton();
    }
    
    private CongTo findCongToById(String SoCT){
        String SQL = "select * from `congto` where `congto`.`SoCT` = '"+SoCT+"'";
        ResultSet rs = connection.ExcuteQueryGetTable(SQL);
        try {
            if(rs.next())
                return new CongTo(rs);
            else
                return null;
        } catch (Exception e) {
            return null;
        }
    }
    
    private void findCongToByChuHo(){
        String SQL = "select * from `congto` where `ChuHo` like '%"+txtSearch.getText()+"%'";
        ResultSet rs = connection.ExcuteQueryGetTable(SQL);
        Object [] obj = new Object[]{"Số CT","Chủ hộ","Số cũ","Số mới","Sô tiêu thụ","Thành tiền"};
        DefaultTableModel tableModel = new DefaultTableModel(obj,0);
        tblCongTo.setModel(tableModel);
        try{
            while(rs.next()){
                Object[] item = new Object[6];
                CongTo hs = new CongTo(rs);
                item[0] = hs.getSoCT();
                item[1] = hs.getChuHo();
                item[2] = hs.getSoCu();
                item[3] = hs.getSoMoi();
                item[4] = hs.getSoDien();
                item[5] = hs.getThanhTien();
                tableModel.addRow(item);
            }
        }
        catch (Exception e) {
            System.out.println(e.toString());
        }
        clearText();
        setWidthColumn();
        disableButton();
    }
    
    private void disableButton(){
        if(tblCongTo.getSelectedRow()==-1){
            btnCapNhat.setEnabled(false);
            btnXoa.setEnabled(false);
        }
        else{
            btnCapNhat.setEnabled(true);
            btnXoa.setEnabled(true);
        }
    }
    
