    private int checkIdExist(){
        try {
            String SQL = "select count(*) as `count` from `congto` where `congto`.`SoCT` = '"+txtSoCT.getText()+"'";
            ResultSet rs = connection.ExcuteQueryGetTable(SQL);
            return rs.next()?rs.getInt("count"):10;
        } catch (SQLException ex) {
            System.out.println("error");
            return 10;
        }
    }
    
    private boolean checkInvalid(){
        if(txtSoCT.getText().isEmpty() || txtChuHo.getText().isEmpty() || txtSoCu.getText().isEmpty() || txtSoMoi.getText().isEmpty() || txtDonGia.getText().isEmpty())
                return false;
        try {
            Integer.parseInt(txtSoCu.getText());
        } catch (Exception e) {
            return false;
        }
        try {
            Integer.parseInt(txtSoMoi.getText());
        } catch (Exception e) {
            return false;
        }
        try {
            Integer.parseInt(txtDonGia.getText());
        } catch (Exception e) {
            return false;
        }
        if(Integer.parseInt(txtSoMoi.getText())<Integer.parseInt(txtSoCu.getText()))
            return false;
        return true;
    }
    
    private boolean checkIdExistToUpdate(int index){
        int count = 0;
        for(int i = 0;i<tblCongTo.getModel().getRowCount();i++){
            if(i == index)
                continue;
            if(txtSoCT.getText().equals(tblCongTo.getModel().getValueAt(i, 0))){
                count ++;
            }
        }
        return count<1;
    }
    
    private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMoiActionPerformed
        // TODO add your handling code here:
        if(checkInvalid()){
            if(checkIdExist()<=0){
                String SQL = "insert into `congto` (`SoCT`, `ChuHo`,`SoCu`,`SoMoi`,`DonGia`) VALUES ('"+txtSoCT.getText()+"','"+txtChuHo.getText()+"','"+txtSoCu.getText()+"','"+txtSoMoi.getText()+"','"+txtDonGia.getText()+"')";
                connection.ExcuteQueryUpdateDB(SQL);
                LoadData();
            }
            else{
                JOptionPane.showMessageDialog(null, "Mã công tơ đã tồn tại!");
            }
        }
        else
            JOptionPane.showMessageDialog(null, "Thông tin không hợp lệ!");
    }//GEN-LAST:event_btnThemMoiActionPerformed

