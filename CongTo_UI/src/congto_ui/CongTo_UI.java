    private void tblCongToMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCongToMouseClicked
        // TODO add your handling code here:
        int index = tblCongTo.getSelectedRow();
        CongTo ct;
        ct = findCongToById(tblCongTo.getValueAt(index, 0).toString());
        txtSoCT.setText(tblCongTo.getValueAt(index, 0).toString());
        txtChuHo.setText(tblCongTo.getValueAt(index, 1).toString());
        txtSoCu.setText(tblCongTo.getValueAt(index, 2).toString());
        txtSoMoi.setText(tblCongTo.getValueAt(index, 3).toString());
        txtDonGia.setText(String.valueOf(ct.getDonGia()));
        disableButton();
    }//GEN-LAST:event_tblCongToMouseClicked

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        if(checkInvalid()){
            if(checkIdExistToUpdate(tblCongTo.getSelectedRow())){
                String SQL = "update `congto` set `ChuHo` = '"+txtChuHo.getText()+"', `SoCT` = '"+txtSoCT.getText() + "', `SoCu` = "+txtSoCu.getText()+ ", `SoMoi` = "+txtSoMoi.getText()+ ", `DonGia` = "+txtDonGia.getText()+" where `SoCT` = '"+tblCongTo.getValueAt(tblCongTo.getSelectedRow(), 0).toString() +"'";
                connection.ExcuteQueryUpdateDB(SQL);
                LoadData();
            }
            else
                JOptionPane.showMessageDialog(null, "Mã công tơ đã tồn tại!");
        }
        else
            JOptionPane.showMessageDialog(null, "Thông tin không hợp lệ!");
    }//GEN-LAST:event_btnCapNhatActionPerformed

